package com.example.scrapping.models;

public class Result {


    private double totalRamUsage;
    private double totalCpuUsage;
    private double totalTime;
    public double getTotalRamUsage() {
        return totalRamUsage;
    }

    public void setTotalRamUsage(double totalRamUsage) {
        this.totalRamUsage = totalRamUsage;
    }

    public double getTotalCpuUsage() {
        return totalCpuUsage;
    }

    public void setTotalCpuUsage(double totalCpuUsage) {
        this.totalCpuUsage = totalCpuUsage;
    }

    public double getTotalTime() {
        return totalTime;
    }

    public void setTotalTime(double totalTime) {
        this.totalTime = totalTime;
    }



}
